fun main(args: Array<String>) {
    //arithmetic operators
    var num1 = 36
    var num2 = 17
    println(num1 + num2)
    println(num1 - num2)
    println(num1 * num2)
    println(num1 / num2)
    println(num1 % num2) //Modulus - Returns the remainder

    //comparison operators
    var num3=59
    var num4=26

    println(num3 < num4)
    println(num3 > num4)
    println(num3 <= num4)
    println(num3 >= num4)
    println(num3 == num4)
    println(num3 != num4)    //not equal to

    //assignment operators

    var num5=72
    println(num5)

    var num6 = 4
    num6+=7
    println(num6)

    var num8 = 51
    num8-=23
    println(num8)

    var num9 = 25
    num9*=25
    println(num9)

    var num7=125
    num7/=5
    println(num7)

    var num10=525
    num10%=25
    println(num10)


    //logical operators

    var x = 80

    println(x<10 && x>20 ) //and
    println(x<10 || x>20 ) //or
    println(!(x<10 || x>20) ) //not






}